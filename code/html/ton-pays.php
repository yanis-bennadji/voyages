            <h2>
                Sport
            </h2>
            <p>
                La France occupe une place de premier plan dans le monde du sport, tant au niveau national qu'international. À l'échelle nationale, le sport fait partie intégrante de la culture française, renforçant l'identité nationale et la fierté. Le pays accueille également des événements sportifs de renommée mondiale, comme le Tour de France et Roland-Garros. Sur la scène internationale, la France est un acteur clé dans les instances dirigeantes du sport et a accueilli plusieurs grands événements sportifs. Enfin, les Français excellent dans divers domaines sportifs, produisant des performances remarquables. Par exemple, des athlètes français ont remporté des médailles d'or aux Jeux olympiques dans des disciplines telles que la natation, l'escrime, et le judo. De plus, des équipes françaises comme l'équipe de football ont remporté des titres majeurs, tels que la Coupe du Monde FIFA, démontrant ainsi la compétitivité et le talent exceptionnels des sportifs français à l'échelle mondiale. 
            </p>
