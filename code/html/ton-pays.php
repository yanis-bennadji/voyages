    <h3>
        Les monuments
    </h3>
    <div class="gbox">
        <div class="boxinvisible">
          <img class="eiffel" src="../../assets/image/malik/eiffel.jpg" alt="Tour eifeeil qui brille">
          <p>
            La Tour Eiffel, emblème emblématique de Paris et de la France, est l'une des structures les plus célèbres au monde, conçue par l'ingénieur Gustave Eiffel et inaugurée en 1889 lors de l'Exposition universelle.
          </p>
        </div>
        <div class="boxinvisible">
          <img class="adt" src="../../assets/image/malik/adt.jpg" alt="Arc de triomphe qui brille">
          <p>
            L'Arc de Triomphe est un monument emblématique situé au cœur de Paris, inaugurée en 1806. Commandé par Napoléon Bonaparte, il célèbre les victoires militaires françaises et honore les soldats tombés au combat. Conçu par l'architecte Jean-François Chalgrin, il est orné de sculptures et d'inscriptions commémoratives. Au sommet de l'arc, une terrasse offre une vue panoramique spectaculaire de la ville. Symbole de la grandeur et de la résilience nationale, l'Arc de Triomphe est un incontournable pour les visiteurs de Paris.
          </p>
        </div> 
        <div class="boxinvisible">
          <img class="versailles" src="../../assets/image/malik/versailles.jpg" alt="chateau de versailles">
          <p>
            Le Château de Versailles, résidence royale emblématique située près de Paris, est un chef-d'œuvre de l'architecture française. Construit au XVIIe siècle sous la direction de Louis XIV, il est renommé pour sa splendeur et son opulence.
          </p>
