    <h2>Plage de Langkawi</h2>
    <p>Au nord-ouest de la Malaisie, à la limite de la frontière thaï, s'étend un archipel de 99 îles féeriques, les îles Langkawi.Les îles Langkawi offrent des paysages resplendissants, sauvages et fantasmatiques : de hautes falaises surmontées de forêts vierges plongent dans la mer, découpant criques, baies et grottes inaccessibles. Aujourd'hui, Langkawi, l'île principale, est un pôle touristique majeur, regroupant complexes hôteliers et gros investisseurs. L’île n'a pas encore totalement perdu son caractère authentique, mais ça ne saurait tarder.</p>
