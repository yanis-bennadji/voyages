    <h2>Kuala Lampur, capitale historique</h2>
    <p>La capitale de la Malaisie saura vous intriguer avec ses tours jumelles. Il est impossible que ces deux édifices vous laissent indifférent: symboles de Kuala Lumpur, elles s’élèvent jusqu’à 452 mètres de hauteur et disposent de 88 étages. Les Tours Petronas regroupent en leur sein des bureaux, des boutiques,un centre multimédia. Les deux tours sont reliées par un skybridge qui se situe à 170 mètres au-dessus du sol. Pour avoir une vue imprenable sur la ville et ses environs, pour comprendre toute la majesté de ce lieu, les Tours Petronas sont absolument à faire à Kuala Lumpur.</p>
