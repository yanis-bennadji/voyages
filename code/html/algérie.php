  <p class="ptimgad">Timgad ou Thamugadi (colonie Marciana Traiana Thamugadi en latin), surnommée la <br> « Pompéi de l'Afrique du Nord »1 est une cité antique située sur le territoire de la commune <br> homonyme de Timgad, dans la wilaya de Batna dans la <br> région des Aurès, au Nord-Est de l'Algérie.</p>
  <p class="ptimgad">Elle fut fondée par l'empereur romain Trajan en 100 et dotée du statut de colonie. <br> Il s'agit de la dernière « déduction de colonie » en Afrique romaine, <br> c'est-à-dire d'une colonie essentiellement peuplée de citoyens romains <br> (souvent d'ailleurs d'anciens soldats). Bâtie avec ses temples, <br> ses thermes, son forum et son théâtre, la ville, initialement d'une superficie <br> de 12 hectares, finit par en occuper plus de 90. Au vu <br> de son excellent état de conservation et du fait qu'on la considérait comme <br> typique d'une ville romaine, Timgad a été classée au patrimoine mondial de l'humanité <br> par l'UNESCO en 1982. <br> La conservation du site soulève cependant un certain nombre de problèmes.</p>
