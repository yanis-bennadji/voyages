<h2 id="capitale">Capitale</h2>
<section class="salger">
  <p class="palger">Alger (en arabe : الجزائر العاصمة, Al-Jazāʾir El ʿĀṣimanote 1,2, en berbère : ⵍⴷⵣⴰⵢⵔ ⵜⴰⵎⴰⵏⴰⵖⵜ, Ldzayer tamanaɣt ou Lezzayer tamanaɣt3), surnommée El Bahdja (« la joyeuse »), El Mahrussa (« la bien-gardée ») ou El Beida (« la blanche »), est la capitale de l'Algérie et en est la ville la plus peuplée.</p>
  <p class="palger">Située au bord de la mer Méditerranée, la ville d'Alger est en fait constituée de plusieurs communes de la wilaya d'Alger dont elle donne son nom en tant que chef-lieu mais n'a ni personnalité juridique, ni structure d'administration en propre. L'unité urbaine d'Alger comptait 2 481 788 habitants selon l'Office national des statistiques algérien d'après le dernier recensement de 20084.</p>
  <p class="palger">Fondée au ive siècle av. J.-C. comme comptoir par les phéniciens, sous le nom d'Icosium, elle est occupée par les Romains, les Vandales, les Byzantins et les Arabes, puis au début du Moyen Âge par la tribu berbère des Beni-Mezghana, avant d'être récupérée en 1204 par la tribu arabe des Thaâliba qui règneront sur la région d'Alger jusqu'en 15168.</p>
