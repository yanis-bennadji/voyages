  <h2 id="geo"> Géographie</h2>
<section class="sgeo">
<p class="pgeo">L'Algérie est un pays constitué d'une multitude de reliefs. Le nord est sillonné d'ouest en est <br> par une double barrière montagneuse (Atlas tellien et saharien) avec des chaînes telles que <br> le Dahra, l'Ouarsenis, le Hodna, les  chaines de Kabylie (le Djurdjura, les Babors et <br> les Bibans) et l'Aurès. Le sol est couvert de nombreuses forêts au centre, <br> vastes plaines à l'est et le Sahara qui représente à lui <br> seul 84 % du territoire. La superficie du pays est la plus grande d'Afrique, avec 2 381 741 km2 .</p>
<p class="pgeo">Avec une superficie de 2 381 741 km2, c'est le dixième plus grand pays au monde et le plus grand pays d'Afrique du monde arabe et du bassin méditerranéen. Elle partage plus de 6 343 km de frontières terrestres, avec la Tunisie au nord-est, la Libye à l'est, le Niger au sud-est, le Mali au sud-ouest, la Mauritanie et le Sahara occidental à l'ouest, et enfin le Maroc au nord-ouest12.</p>
